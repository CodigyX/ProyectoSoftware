    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        // Capturar los datos de la interfaz
        String claveEmpleado = txtClaveEmpleado.getText();
        String fecha = txtFecha.getText();
        String hora = txtHora.getText();
        String tipo = txtTipo.getText().toLowerCase();

        // Validar que no haya campos vacíos
        if (claveEmpleado.isEmpty() || fecha.isEmpty() || hora.isEmpty() || tipo == null) {
            System.out.println("Por favor, complete todos los campos.");
            return;
        }

        // Crear un objeto Asistencia con los datos de la interfaz
        Asistencia asistencia = new Asistencia(claveEmpleado, fecha, hora, tipo);

        // Llamar al método registrarAsistencia del controlador
        String respuesta = asistenciaController.registrarAsistencia(asistencia);

        // Mostrar la respuesta al usuario
        JOptionPane.showMessageDialog(this, respuesta);

        // Limpiar los campos de texto después de registrar
        limpiar();
    }//GEN-LAST:event_btnRegistrarActionPerformed

