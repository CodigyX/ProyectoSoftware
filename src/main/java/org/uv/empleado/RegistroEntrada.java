    private void setFechaHoraActual() {
        java.time.LocalDateTime now = java.time.LocalDateTime.now();
        txtFecha.setText(now.format(DateTimeFormatter.ofPattern("yyyy/MM/dd")));
        txtHora.setText(now.format(DateTimeFormatter.ofPattern("HH:mm:ss")));

        // Obtener la hora actual y determinar el tipo de asistencia
        int hour = now.getHour(); // Hora actual en formato 24h

        if (hour >= 0 && hour < 12) {
            txtTipo.setText("Entrada");
        } else if (hour >= 12 && hour < 24) {
            txtTipo.setText("Salida");
        }
    }

    private void limpiar() {
        txtClaveEmpleado.setText("");
        setFechaHoraActual();

    }

